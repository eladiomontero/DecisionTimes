Welkom op dit experiment!

<br><br><br>

Wij vragen U om de volgende regels in acht te nemen:

<br><br>

Gelieve niet te praten met de ander deelnemers.

<br><br>

Zet uw GSM uit en plaats uw materiaal aan de kant. Alleen de documenten die wij hebben gegeven en een pen mogen blijven liggen op de tafel.

<br><br>

Als je een vraag hebt, steek dan je hand op.

<br><br>

Gelieve de onderstaande knop NIET IN TE DRUKKEN vooraleer de onderzoeker je daartoe de opdracht heeft gegeven.

<br><br>

Je hebt een envelop ontvangen met daarin je gebruikersnaam en een paswoord, een deelnameformulier waarop je je bankgegevens zal moeten invullen, en een papier waarop uitgelegd staat hoe het experiment werkt.  De bankgegevens (IBAN en BIC) zijn nodig voor de uitbelating en zullen NIET digitaal bewaard worden.

<br><br> 

De gebruikersnaam is enkel gekend door jou.  De onderzoekers weten niet welke gebruikersnaam aan jou is toegekend of aan de andere deelnemers in dit experiment. 
Hou dit papier bij jou.  Op het einde van het experiment zal je dit enkel moeten tonen zodat de onderzoekers kunnen nagaan wat het juiste bedrag is dat je hebt bekomen in het experiment.

<br><br> 
Je mag nu al je naam en bankgegevens invullen op het deelnameformulier.

Op het einde van het experiment zal op het scherm het bedrag getoond worden dat je hebt gewonnen.  Dit bedrag kan je dan op je deelnameformulier invullen.  De onderzoeker zal je roepen om de transactie te vervolledigen.

<br><br>
Zodra je bent ingelogd met je gebruikersnaam en paswoord volgt er een introductie tot het experiment.
Op het einde van deze uitleg zal er een kleine test worden gedaan die zal nagaan of je het experiment hebt begrepen.  De instructies staan ook opgeschreven in het document dat je hebt ontvangen.  Je kan dit terug bekijken tijdens het experiment, indien nodig.

